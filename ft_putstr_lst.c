#include "libft.h"

void			ft_putstr_lst(char *s, t_list **li)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_lst(s[i], li);
		i++;
	}
}
